package model.data;
  
public class DataDataBean {
	private int data_no;
	private int data_emp_no;
	private String data_title;
	private String data_content;
	private String data_date;
	private String data_file_name;
	private String data_file_addr;
	private String emp_name;
	
	public int getData_no() {
		return data_no;
	}
	public void setData_no(int data_no) {
		this.data_no = data_no;
	}
	public int getData_emp_no() {
		return data_emp_no;
	}
	public void setData_emp_no(int data_emp_no) {
		this.data_emp_no = data_emp_no;
	}
	public String getData_title() {
		return data_title;
	}
	public void setData_title(String data_title) {
		this.data_title = data_title;
	}
	public String getData_content() {
		return data_content;
	}
	public void setData_content(String data_content) {
		this.data_content = data_content;
	}
	public String getData_date() {
		return data_date;
	}
	public void setData_date(String data_date) {
		this.data_date = data_date;
	}
	public String getData_file_name() {
		return data_file_name;
	}
	public void setData_file_name(String data_file_name) {
		this.data_file_name = data_file_name;
	}
	public String getData_file_addr() {
		return data_file_addr;
	}
	public void setData_file_addr(String data_file_addr) {
		this.data_file_addr = data_file_addr;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
}
